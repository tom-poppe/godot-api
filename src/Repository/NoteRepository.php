    public function findAllWithPagination(int $page, User $user): Paginator
    {
        $query = $this->createQueryBuilder('n')
            ->where('n.user = :user')
            ->setParameter('user', $user)
            ->orderBy('n.createdAt', 'DESC');

        return new Paginator($query, $page);
